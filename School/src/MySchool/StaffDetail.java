    private void showDetail(String order){              
        int tot=0;
        int yr=0;
        try{            
                rs=null;
                pstmt=MyLogin.con.prepareStatement("select ID,FULL_NAME,FATHER_NAME,ADDRESS,JOIN_DATE,RESIGN_DATE from STAFF_DETAIL_TABLE order by "+order+"");                                     
                rs=pstmt.executeQuery();
                pstmt.clearParameters();                 
           
            DefaultTableModel tm=(DefaultTableModel)Table.getModel(); 
               
        //    for(;tm.getRowCount()>0;tm.removeRow(0));
            while(tm.getRowCount()>0){                    
                    tm.removeRow(0);
                }        
            tot=0;       
            while(rs.next()){
                int flag=0,join_year=0,resign_year=0;
                yr=0;
                String santosh="",resigndate="";
                    
                    yr=YearChooser.getYear();                    
                    santosh=rs.getString("JOIN_DATE");
                    join_year=Integer.parseInt(santosh.substring(7));
                    if(yr>=join_year){       
                        resigndate=rs.getString("RESIGN_DATE");
                        if(resigndate.equalsIgnoreCase("-"))
                            flag=1;
                        else{
                            resign_year=Integer.parseInt(resigndate.substring(7));
                            if(resign_year>=yr)
                                flag=1;
                        }
                    }
                    if(flag==1){
                        tot++;                                                
                        Object kk[]={rs.getString("ID"),rs.getString("FULL_NAME"),rs.getString("FATHER_NAME"),rs.getString("ADDRESS"),rs.getString("JOIN_DATE"),rs.getString("RESIGN_DATE")};
                        tm.addRow(kk);   
                    }
            }
            msgLabel.setText("LIST OF TEACHERS IN THE YEAR : "+yr);
            totLabel2.setText(""+tot);           
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Invalid Entry (year/date) ", "Detail", JOptionPane.ERROR_MESSAGE);           
        }      
    }
    
