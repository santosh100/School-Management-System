    public void checkLetters(java.awt.event.KeyEvent evt,String letters){
        char cr=evt.getKeyChar();
        if(!((Character.isLetter(cr))||(cr==KeyEvent.VK_BACKSPACE)||(cr==KeyEvent.VK_SPACE))){
            getToolkit().beep();
            evt.consume();
        }
        if(letters.length()>49){
                getToolkit().beep();
                evt.consume();                  
        }
    }   
    
    /**
     * @param evt*
     * @param fieldname**************************************************************************************/
    public void checkNumbers(java.awt.event.KeyEvent evt,javax.swing.JTextField fieldname){
        char cr=evt.getKeyChar();
        if(!(Character.isDigit(cr) || (cr==KeyEvent.VK_BACKSPACE)||cr==KeyEvent.VK_DELETE)){
            getToolkit().beep();
            evt.consume();
        }
        if(fieldname.getText().length()>9){
            getToolkit().beep();
            evt.consume();                   
        }
    }
    
    /**
     * @param evt*
     * @param fieldname*************************************************************************************/
    public void checkForPercentage(java.awt.event.KeyEvent evt,javax.swing.JTextField fieldname){
        char cr=evt.getKeyChar();
        System.out.println(""+cr);
        if(!(Character.isDigit(cr)||((int)cr==46)||cr==KeyEvent.VK_BACKSPACE)){
            getToolkit().beep();
            evt.consume();
        }
        if(fieldname.getText().length()>4){
            getToolkit().beep();
            evt.consume();
        }
    } 
    
